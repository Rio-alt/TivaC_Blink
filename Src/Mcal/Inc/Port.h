/**********************************************************************************************************************
 *  GLOBAL DATA TYPES AND STRUCTURES
 *********************************************************************************************************************/


typedef enum {
    DIO         ,
    UART        ,
    I2C             
}PinMode;



typedef enum {
    INPUT       ,
    OUTPUT
}PinDir;

typedef enum {
    PUR         ,
    PDR         ,
    Open_Drain 
}PinAttach;

typedef enum {
    mA_2        ,
    mA_4        ,
    mA_8
}PinCurrent;


typedef struct Pin{

ChannelType     Pin         ;
PinMode         Mode        ;
LevelType       PinValue    ;
PinDir          Dir         ;
PinAttach       Attach      ;
PinCurrent      Current     ;

}PinConfig;

/**********************************************************************************************************************
 *  GLOBAL DATA PROTOTYPES
 *********************************************************************************************************************/

extern PinConfig pins[];


